function getDuDoanFromPattern(pattern) {
  const keys = Object.keys(PATTERN_MAP).sort((a, b) => b.length - a.length);
  for (const key of keys) {
    if (pattern.endsWith(key)) return { du_doan: PATTERN_MAP[key], khop_pattern: key };
  }
  return { du_doan: "?", khop_pattern: null };
}

function getTX(d1, d2, d3) {
  const sum = d1 + d2 + d3;
  return sum >= 11 ? "T" : "X";
}

// Lấy dữ liệu từ API bên ngoài mỗi 5 giây
async function fetchData() {
  try {
    const res = await axios.get("https://apigame-wy0p.onrender.com/api/sunwin");
    const data = res.data;

    if (!data || !data.Tong || !data.Xuc_xac1 || !data.Xuc_xac2 || !data.Xuc_xac3 || !data.Phien) {
      console.log("❌ Dữ liệu API không hợp lệ:", data);
      return;
    }

    const d1 = parseInt(data.Xuc_xac1);
    const d2 = parseInt(data.Xuc_xac2);
    const d3 = parseInt(data.Xuc_xac3);

    rikResults.unshift({
      sid: parseInt(data.Phien),
      d1,
      d2,
      d3,
    });

    rikResults = rikResults.slice(0, 50); // giữ 50 phiên gần nhất
  } catch (err) {
    console.error("❌ Lỗi khi lấy dữ liệu:", err.message);
  }
}

fetchData();
setInterval(fetchData, 5000);

fastify.register(cors);

// API dự đoán
let pattern = [];

fastify.get("/axobantol", async () => {
  try {
    const res = await fetch("https://apigame-wy0p.onrender.com/api/sunwin");
    const data = await res.json();

    if (!data || !data.ket_qua || !data.phien || !data.xuc_xac1 || !data.xuc_xac2 || !data.xuc_xac3) {
      console.warn("❌ Dữ liệu API không hợp lệ:", data);
      return { message: "Không đủ dữ liệu." };
    }

    // Thêm kết quả mới vào pattern
    const ketQua = data.ket_qua.toLowerCase() === "tài" ? "t" : "x";
    pattern.push(ketQua);
    if (pattern.length > 13) pattern.shift(); // Giữ tối đa 13 kết quả gần nhất

    const patternStr = pattern.join("");
    const { du_doan, khop_pattern } = getDuDoanFromPattern(patternStr.toUpperCase());

    return {
      id: "@axobantool",
      phien_cu: data.phien,
      ket_qua: data.ket_qua,
      xuc_xac: `${data.xuc_xac1},${data.xuc_xac2},${data.xuc_xac3}`,
      phien_moi: data.phien + 1,
      pattern: patternStr,
      khop_pattern,
      du_doan
    };
  } catch (err) {
    console.error("❌ Lỗi API:", err);
    return { message: "Lỗi khi gọi API." };
  }
});

// Start server
const start = async () => {
  try {
    const address = await fastify.listen({ port: PORT, host: "0.0.0.0" });
    console.log(`🚀 Server chạy tại ${address}`);
  } catch (err) {
    console.error("❌ Server lỗi:", err);
    process.exit(1);
  }
};

start();
